public class Skills {

    private String skillName;
    private int skillAttact;

    public String getSkillName() {
        return skillName;
    }

    public void setSkillName(String skillName) { this.skillName = skillName;
    }

    public int getSkillAttact() {
        return skillAttact;
    }

    public void setSkillAttact(int skillAttact) {
        this.skillAttact = skillAttact;
    }
}
